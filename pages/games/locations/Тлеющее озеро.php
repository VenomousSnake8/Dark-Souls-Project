            <h2>Описание</h2>
            <p>Большое по размерам озеро, которое потихоньку высушивается. Чтобы попасть в эту локацию, нужно разрушить верёвочный мост, который ведёт к <a href="../bosses/Верховный повелитель Вольнир.php">Верховному повелителю Вольниру</a>, потом спуститься по мосту и убить <a href="#">демона</a>, после чего пройти вниз, активировать <a href="#">костёр</a> и пройти чуть дальше. При выходе на озеро вас будет поджидать <a href="#">Автоматическая баллиста</a>, которая будет стрелять большими копьями сверху, поэтому будьте осторожны. Если попытаться пройти в сторону <a href="#">туманных врат</a>, которые ведут к <a href="../bosses/Старый король демонов.php">боссу</a>, то появится <a href="#">огромный червь</a>, который начнёт выпускать в вас разряды молнии, наносящие колоссальное количество урона.</p>
            <p>Баллиста стреляет сверху, со стороны <a href="#">крабов</a>, залпом из трёх огромных снарядов. Чтобы избежать попадания, можно прятаться за деревьями и перемещаться по локации в паузах после окончания тройного залпа.</p>
            <h2>Костры</h2>
            <ul>
                <li><b>Прихожая старого короля</b> — нужно бежать к середине озера и найти место с каменным полом. Выстрел из баллисты проломит его, и вы провалитесь вниз; далее поворачиваете налево и идёте по коридору, навстречу вам может выйти <a href="#">демон</a> (его можно пробежать/убить), потом сворачиваете влево, там вас будет ожидать первый костёр.</li>
                <li><b>Руины демона</b> — справа от входа к боссу локации есть проход; спустившись по лестнице, можно найти костёр.</li>
                <li><b>Старый король демонов</b> — костёр появляется после победы над <a href="../bosses/Старый король демонов.php">боссом локации</a>.</li>
            </ul>
