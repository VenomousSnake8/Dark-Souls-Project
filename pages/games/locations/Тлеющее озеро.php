            <h2>Примечания</h2>
            <ul>
                <li>Перед тем, как отключать баллисту, есть смысл сначала с её помощью разбить стену, которая находится на другой стороне озера напротив и чуть правее входа в локацию. За ней лежит <a href="#">Кольцо с пёстрым камнем</a>.</li>
                <li>Если вы отключили баллисту, вы сможете её снова включить, а потом выключить в любой момент.</li>
                <li><a href="#">Песчаного червя Картуса</a> можно убить баллистой. Для этого отлично подойдёт валун справа — если смотреть от двери — на площадке перед входом на арену к боссу локации. Стоять нужно не совсем вплотную, иначе урон от баллисты всё равно будет проходить.</li>
                <li>От костра <b>Прихожая старого короля</b> спускаемся вниз и разбиваем <a href="#">иллюзорную стену</a>, находим <a href="#">чёрного рыцаря</a> с <a href="#">большой секирой</a>, сражающегося с мозгляком. Рыцарь охраняет <a href="#">меч чёрного рыцаря</a>.</li>
                <li>При входе в локацию из Катакомб Картуса, держа путь вправо вдоль стены, вы найдёте пещерку, в которой вас ждут две <a href="#">ящерки</a> и <a href="#">Гораций Молчаливый</a>, вооружённый <a href="#">алебардой</a> и <a href="#">малым щитом</a>. Это место можно увидеть с ветхого моста перед <a href="../bosses/Верховный повелитель Вольнир.php">Вольниром</a>.</li>
