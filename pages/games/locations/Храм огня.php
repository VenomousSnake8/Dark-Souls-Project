            <p><b>Храм огня</b> (англ. <i>Firelink Shrine</i>) &mdash; локация в игре <a href="../Dark Souls III.php">Dark Souls III</a>.</p>
            <h2>Описание</h2>
            <p>Храм огня — это, своего рода, основная база в игре, куда игрок будет возвращаться раз за разом, чтобы повысить свой уровень, улучшить <a href="#">флягу с эстусом</a> и контактировать с другими NPC, которые будут перемещаться сюда после встречи игрока с ними в других локациях.</p>
            <h2>Костры</h2>
            <p><b>Храм огня</b> — уникальный <a href="#">костёр</a>, на котором можно сжечь <a href="#">Горящий осколок кости</a>, чтобы усилить флягу с эстусом. Для того, чтобы его разжечь, вам необходимо вставить <a href="#">Витой меч</a> в нишу на полу по центру локации. Можно заметить, что Витой меч вставляется в потрёпанную временем <b>Великую Чашу</b>, с помощью которой игрок перемещался между кострами в первой части.</p>
