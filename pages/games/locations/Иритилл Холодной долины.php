            <p><b>Иритилл Холодной долины</b> (англ. <i>Irithyll of the Boreal Valley</i>) &mdash; локация в игре <a href="../Dark Souls III.php">Dark Souls III</a>.</p>
            <h2>Описание</h2>
            <p>Покрытый льдом и снегом город, освещаемый только светом Луны, навечно застывшей над ним. Когда-то этот город был основан вокруг древнего <a href="Анор Лондо.php">Анор Лондо</a> и был домом для аристократов, поклонявшихся Луне, но сейчас единственными его жителями являются истерзанные духи, превратившиеся в тени самих себя под руководством <a href="../bosses/Понтифик Саливан.php">Понтифика</a>. Город закрыт для посторонних и надёжно охраняется, попасть туда могут только те, кому была оказана честь носить артефакт — <a href="#">Маленькую куклу</a>, за владение которой вам придётся побороться с <a href="../bosses/Дьяконы глубин.php">Дьяконами глубин</a>.</p>
            <p>В Иритилле существует орден <a href="#">Клинков Тёмной луны</a>, члены которого являлись праведными мстителями и служили <b>Гвиндолину Тёмное Солнце</b>. В настоящее время, из-за захвата Иритилла Саливаном, орден является лишь тенью себя прежнего.</p>
            <p>В Иритилле создают оружие и броню, закалённые морозом. Во всём остальном мире такое оружие считается редкостью.</p>
            <h2>Костры</h2>
            <ul>
                <li><b>Иритилл Холодной долины</b> — сразу после выхода из <a href="../locations/Катакомбы Картуса.php">Катакомб Картуса</a>, перед мостом, ведущим в Иритилл.</li>
                <li><b>Центральный Иритилл</b> — на площади, находящейся сразу после моста и магической стены, не пускающей посторонних в Иритилл.</li>
                <li><b>Церковь Йоршки</b> — находится в центре города. Найти данный костёр можно, повернув налево от моста, где улицы патрулирует <a href="#">огненная ведьма</a> в сопровождении двух <a href="#">рыцарей понтифика</a>.</li>
                <li><b>Дальнее поместье</b> — на краю болота, простирающегося под мостом в Иритилл. Чтобы его найти, нужно пройти чуть дальше канализации, по которой мы попадаем в окрестности храма Понтифика.</li>
                <li><b>Понтифик Саливан</b> — костёр появляется после победы над <a href="../bosses/Понтифик Саливан.php">боссом локации</a>.</li>
                <li><b>Резервуар с водой</b> — появится после убийства двух <a href="#">Саливанских Чудищ</a>, охраняющих <a href="#">Архидьякона МакДоннелла</a>.</li>
                <li><b>Тюремная башня</b> — в башне, в которой томится <a href="#">Капитан отряда Йоршка</a>. Добраться к ней можно, пройдя по невидимой тропинке.</li>
            </ul>
