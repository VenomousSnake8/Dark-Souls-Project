            <p><b>Пик Древних Драконов</b> (англ. <i>Archdragon Peak</i>) &mdash; секретная локация в игре <a href="../Dark Souls III.php">Dark Souls III</a>.</p>
            <h2>Описание</h2>
            <p>Чтобы найти эту секретную локацию, вы должны убить <a href="../bosses/Оцейрос.php">Оцейроса, Снедаемого короля</a>. Откройте дверь за комнатой, где был бой с боссом. У разбитого фонтана лежит тело рыцаря крови дракона, у него берём <a href="#">жест</a> <b>Путь дракона</b>. Затем отправляйтесь в <a href="Подземелье Иритилла.php">Подземелье Иритилла</a>, там на открытой площадке рядом с доспехами рыцарей и сидит окаменевший последователь Пути дракона. При помощи жеста <b>Путь дракона</b> сядьте рядом с ним и подождите, пока вас перенесут в новую локацию. На <a href="#">NG+</a> можно войти в эту локацию без убийства босса при условии, что жест <b>Путь дракона</b> был подобран на NG.</p>
            <h2>Костры</h2>
