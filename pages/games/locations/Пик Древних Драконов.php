                <li><b>Большая колокольня</b> — недалеко от огромного колокола, звон в который вызовет <a href="../bosses/Безымянный король.php">Безымянного короля</a>.</li>
                <li><b>Безымянный король</b> — открывается после победы над одноимённым боссом.</li>
            </ul>
