            <h2>Примечания</h2>
            <ul>
                <li>Данную локацию можно увидеть из разных мест в землях <a href="#">Лотрика</a>: площадка после боя с <a href="../bosses/Вордт из Холодной долины.php">Вордтом</a>, площадка после боя с <a href="../bosses/Понтифик Саливан.php">Понтификом Саливаном</a> и далее до <a href="Анор Лондо.php">Анор Лондо</a>, площадка в <a href="Подземелье Иритилла.php">Подземелье Иритилла</a>, где происходит перенос в локацию.</li>
                <li>После призыва Безымянного короля посредством активации колокола по всей локации распространится туман и буря, которые исчезнут только после убийства босса. Арена битвы с самим боссом находится на уровень выше самой локации и является своеобразным "полом из ветра".</li>
                <li>С края скалы, у гроба, где берётся мерцающий камень драконьего туловища, можно увидеть труп гигантского дракона, наполовину вросший в гору напротив. По-видимому это тот самый гигантский дракон, которому были поднесены мерцающие камни драконьего туловища и головы.</li>
