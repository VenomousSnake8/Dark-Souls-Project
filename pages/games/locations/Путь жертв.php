            <h2>Описание</h2>
            <p>Путь жертв — старая дорога, по которой ранее перевозили нежить в <a href="Храм глубин.php">Храм глубин</a>. Эта дорога проходит у окраины <a href="Цитадель Фаррона.php">Цитадели Фаррона</a>, где обитают <a href="../bosses/Знаток кристальных чар.php">Знаток кристальных чар</a> и его подчинённые полые.</p>
            <p>Путь жертв состоит из извилистых лесных дорожек, мрачных трясин и руин замков. Встречающиеся в локации враги будут наносить эффекты отравления, кровотечения или магический урон. Обязательно проверьте устойчивость вашего персонажа к данным эффектам, чтобы они не застали вас врасплох.</p>
