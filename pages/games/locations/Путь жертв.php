            <h2>Примечания</h2>
            <ul>
                <li>Если при появлении красного фантома <a href="#">Хейзел Жёлтый Палец</a> призвать фиолетового фантома <a href="#">Праведного рыцаря Годрика</a> (знак призыва расположен недалеко от костра <b>Лес мучений</b>), то можно стравить их друг с другом. Бой будет недолгим, но полученные души и предметы достанутся протагонисту. Имея при себе несколько <a href="#">семян древа гигантов</a> и немного удачи, вполне возможно зачистить всех проблемных противников на пути жертв.</li>
                <li>Ядовитые жуки могут доставить массу хлопот, если пытаться убить всех сразу с наскока. Не торопитесь, иначе они могут окружить персонажа и обездвижить быстрыми атаками, а также отравить.</li>
                <li>Чтобы остановить вторжения фантомов в локации, недостаточно убить одного Знатока кристальных чар. Необходимо также победить и <a href="../bosses/Хранители Бездны.php">Хранителей Бездны</a>.</li>
            </ul>
