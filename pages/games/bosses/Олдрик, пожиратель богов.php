                <li>Наконечник оружия — это иллюзия. Олдрик всё время держит в руках <a href="#">Золотое ритуальное копьё</a>.</li>
                <li>По раннему замыслу разработчиков он должен был не идти в Иритилл, а вылезать из своего гроба во второй фазе боя с <a href="Дьяконы глубин.php">Дьяконами глубин</a>.</li>
