            <h2>История</h2>
            <p>Некогда Олдрик был праведным клириком Белого Пути, родом из <a href="../locations/Иритилл Холодной долины.php">Иритилла</a>. Но постепенно в нём развилась тяга к пожиранию человеческой плоти. Со слов <a href="#">Дезертира Хоквуда</a>, он ел так много, что со временем раздулся, как утопленная свинья, а затем размягчился в аморфную субстанцию. Поэтому «они» поместили его в <a href="../locations/Храм глубин.php">Храм Глубин</a>. Глубины изначально были тихим и священным местом, но там нашли пристанище многие ужасные чудовища. Кто «они», Хоквуд не уточняет, но, скорее всего, речь идёт о богах.</p>
            <p>Олдрик не оставил своё увлечение человеческой плотью, и постепенно вокруг него в Храме Глубин сформировался целый культ. В него во многом вошли те, кто служил в Храме: <a href="Дьяконы глубин.php">священники, сломленные мощью Глубин</a>. Их <a href="#">священными фолиантами</a> стали осквернённые трактаты старой веры. Одеяния, ритуальные предметы и архитектурный стиль строений культа напоминают о более благородных религиях старых времён, но в основе этой веры лежало скармливание Олдрику всё новой плоти. <a href="#">Проповедники</a> Храма, обещавшие спасение простым людям, на самом же деле отправляли их по <a href="../locations/Путь жертв.php">пути жертв</a> на съедение Олдрику, а останки закапывали на территории вокруг Храма Глубин.</p>
            <p>Когда пришло время угасания огня, Олдрика сделали Повелителем Пепла не за его достоинства, а за его размеры. Так, из веры Белого Пути возникла новая, извращённая вера, а Олдрик стал святым покровителем Глубин.</p>
            <p>Восстав по зову колокола, бывший клирик отправился на свою родину, в Иритилл Холодной долины. Там <a href="Понтифик Саливан.php">Понтифик Саливан</a> запер для него бога старой королевской династии в заброшенном соборе. Придя к трону через поедание плоти, святой покровитель Глубин разочаровался в нём и стал Пожирателем Богов.</p>
            <p>Самые верные из дьяконов, в частности, <a href="#">архидьякон МакДоннелл</a>, отправились вслед за Олдриком в Иритилл, дабы с <a href="#">ковенантом Верных Олдрику</a> защищать своего идола от всех, кто решит прервать его трапезу, и терпеливо ждать его возвращения.</p>
