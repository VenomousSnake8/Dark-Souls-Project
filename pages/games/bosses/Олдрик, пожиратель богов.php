            <h2>Атаки</h2>
            <ul>
                <li>Выпускает в разные стороны самонаводящиеся сгустки души, которые через некоторый промежуток времени летят в игрока. Наносят урон от небольшого до среднего (если попали все сразу). Легко уклониться.</li>
                <li>Атакует двумя <a href="#">копьями души</a> в игрока (иногда одним), траектория полёта копий не меняется. Вторая фаза: к магическому урону добавляется огненный, а визуально копьё Олдрика начинает гореть. Наносит от среднего (в первой стадии) до большого (во второй) количество урона. Довольно просто уклониться перекатом. Иногда босс может «озвереть» и выпускать копья по 4 и более раз подряд, что обычно происходит во второй фазе.</li>
                <li>Поднимает лук над головой и выпускает большое количество стрел в конусе перед собой. Вторая фаза: выпускает дождь стрел, преследующий игрока; стрелы бьют по циклу, три раза (три раза меняют направление) и имеют малый промежуток, во время которого можно перекатываться. Стрелы наносят огромный урон и могут очень быстро убить игрока, полностью увернуться от них почти невозможно (частично достают персонажа даже за колоннами), стоит делать 2-3 переката (либо просто убегать) в одну сторону, после чего менять направление (если стрелы во второй фазе попали по вам, они возвращаются к боссу).</li>
                <li>Создаёт червоточину в полу и через некоторое время ныряет в неё, после появляется в противоположном от места создания углу. Если находиться близко к червоточине, когда босс ныряет в неё, можно получить урон (стоя у самого конца хвоста, урон вы не получите). Вторая фаза: к телепортации добавляется атака самонаводящимися сгустками души, также есть небольшое тёмное AOE (босс ударяет посохом по земле).</li>
                <li>Вторая фаза: после непродолжительного каста бьёт заклинанием <a href="#">Коса охоты на жизнь</a>, наносящим круговой удар, при попадании восстанавливающим боссу некоторое количество здоровья. Босс использует эту атаку довольно редко. Может провести два полных оборота косой за один раз, урон от такой атаки разрушителен, а босс при попадании обоими оборотами восполняет много здоровья.</li>
                <li>Босс проводит одну или две размашистые горизонтальные атаки, после которых может прыгнуть вперёд и нанести мощный вертикальный удар.</li>
                <li>Ныряет вперёд, нанося колющий удар своим копьём.</li>
            </ul>
            <h2>Тактика</h2>
            <ul>
                <li>Босс уязвим к огню, молниям, <a href="#">кровотечению</a> и <a href="#">обморожению</a>, а также получает повышенный урон от <a href="#">меча Фаррона</a> и <a href="#">Меча волчьего рыцаря</a>.</li>
                <li>Устойчив к магии и тьме.</li>
                <li>Самая простая тактика в битве с боссом — постоянно идти на сближение и находиться позади у хвоста. В этом случае босс практически не будет атаковать магией, а скорее начнёт размахивать оружием (от которого легко уклониться). Босс постоянно будет убегать в червоточину (чем ближе вы к туловищу, тем больше шанс получить урон от червоточины) и появляться в одном из четырёх углов зала. Поэтому во время появления лучше всего находиться в центре зала, чтобы как можно быстрее сократить дистанцию. Атаковать нужно с двух рук оружием, пропитанным огнём или молниями, к магии и тьме босс устойчив. Данная тактика подходит и ко второй фазе, разница лишь в том, что под боссом будет гореть огонь, который мешает добраться до хвоста.</li>
                <li>Также отличным вариантом будет быстрое оружие, как например <a href="#">одноручные мечи</a> или <a href="#">изогнутые мечи</a>, покрытые огненным зачарованием. Если повезёт, то, в таком случае, за одну комбоатаку можно отправить босса в червоточину до того, как он успеет сильно навредить игроку.</li>
                <li>Есть возможность нанести боссу большой урон, если атаковать не его хвост, а туловище. Сражаясь с ним лицом к лицу, можно заметно уменьшить количество телепортаций босса, а от атак можно уходить перекатами.</li>
                <li>Основные заклинания и рукопашные атаки босса наносят магический урон, несмотря на их пурпурный цвет, больше свойственный тёмной магии. Из этого следует, что в бою пригодятся предметы с высоким сопротивлением магии, а чудо <a href="#">Большой магический барьер</a> вообще позволит не бояться его магии и идти с ним в размен атаками.</li>
            </ul>
            <h2>Примечания</h2>
            <ul>
                <li>Если вплотную встать к <a href="#">туманным вратам</a> или с помощью консольных команд залететь на арену к боссу, не вызывая его агрессии, то можно услышать как Олдрик (или, возможно, сам Гвиндолин) стонет.</li>
                <li>После того, как Пожиратель Богов будет повержен, в продаже у <a href="#">служанки при храме</a> появится <a href="#">сет Смоуга</a>.</li>
