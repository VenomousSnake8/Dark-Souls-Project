            <h2>История</h2>
            <p>Отец Ариандель — нынешний хранитель Нарисованного мира, прежде чем тот сгорит и очистится от гнили. Но он всячески страшится этого и, сидя привязанный к своему трону, он истязает себя, пытаясь задобрить пламя в чаше своей собственной кровью.</p>
            <p>Фриде (в прошлом <b>Эльфриде</b>) — старшая из трех сестёр‐основательниц Чёрной церкви <a href="#">Лондора</a> и <a href="#">Негорящая</a>, отказавшаяся от своего долга. Когда‐то давно она попала в <a href="../locations/Нарисованный мир Арианделя.php">Нарисованный мир Арианделя</a> и, следуя примеру его хозяина, решила остаться здесь и способствовать его гниению. Даже здесь, в Нарисованном мире, ей верой и правдой служит <a href="#">Сир Вильгельм</a> — лондорский рыцарь.</p>
            <h3>Первая встреча</h3>
            <p>Впервые вы встретите Фриде в часовне Арианделя, она будет сидеть в окружении множества картин. При разговоре с ней просит героя покинуть картину и вернуться к исполнению своего долга (диалоги отличаются в зависимости от того, активен или нет квест <a href="#">секретной концовки</a>). Также подарит <a href="#">Кольцо холодящего укуса</a> как подарок от холодного мира. Если ударить Фриде при первой встрече, она тут же исчезает, но после того, как вы отдохнёте у костра, она появляется вновь.</p>
