            <div id="cat1-divt">
                <p>Бой ведётся с одной лишь Фриде. На неё крайне не рекомендуется бежать, когда она встаёт в боевую стойку с косой, так как скорость её атак очень высока. Но атака двуручным мечом, если правильно выбрать расстояние, успевает обездвижить её раньше, чем она сделает удар из стойки. Таким образом, можно провести серию из двух ударов и не получить урон. Лучше не стрелять в неё с помощью магии, так как она будет отскакивать, но если она отскочила назад, следующий выстрел попадёт в неё со 100% вероятностью. Атака из невидимости поначалу кажется непредсказуемой и опасной, в действительности всё ровно наоборот: когда Фриде махнёт косой и исчезнет, следите за столбом пыли, если он вертикален, то она окажется ровно за вами; стоит близко подойти, и она станет видимой, причём подготовки к атаке она не прервёт, это отличный способ нанести несколько ударов, тем самым прервав подготовку её атаки.</p>
                <p>Когда она входит в невидимость (первая/третья фаза), имеет смысл сразу же бежать к ней — при приближении игрока Фриде становится видимой, и можно увидеть, куда она отскочит. Можно попытаться успеть заметить Фриде в момент атаки, но на это рассчитывать не стоит — времени на уклонение слишком мало. Если не удалось определить местоположение Фриде, остаётся лишь ориентироваться по звуку. Сперва будет глубокий вздох — приготовьтесь; затем звук лезвия — уклоняйтесь.</p>
                <p>Фриде можно обездвижить, нанося две‐три (иногда четыре) атаки подряд. Если Фриде зажать возле стены, то она может не отпрыгнуть от игрока, а применить особую круговую атаку косой, которую нельзя прервать. Сориентироваться достаточно сложно: никаких особых звуков перед этой атакой Фриде не издаёт, замах аналогичен обычным прерываемым атакам. Просто не зажимайте её у стен. В случаях, когда ей есть куда отступать, при ударе по ней с потерей баланса она не начинает новое комбо, а отпрыгивает, тем самым давая выпить эстус. Однако, при низком уровне здоровья (её здоровья) начинает вести себя более агрессивно и зачастую не отпрыгивает, а пытается контратаковать. Это даст вам возможность провести еще пару ударов (сбив атаку Сестры), но кроме этого заставляет следить за выносливостью, так как если она закончится, Фриде нанесёт вам очень много урона.</p>
                <p>Если вы способны отследить перемещения Сестры в невидимости, то оптимальным вариантом будет нанесение ей удара в спину с последующей заряженной силовой атакой.</p>
