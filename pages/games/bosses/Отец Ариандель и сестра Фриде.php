            <h2>Бой</h2>
            <h3>Первая фаза</h3>
            <p>Фриде сражается, а Ариандель, по просьбе Фриде, закрывает глаза, чтобы не видеть происходящего. Фриде активно использует свою косу, магию льда и невидимость, чем очень походит на <b>Присциллу</b> из <b>Нарисованного мира Ариамис</b> из первой части, но намного шустрее.</p>
            <h3>Вторая фаза</h3>
            <p>После убийства Фриде Ариандель впадает в бешенство и отрывается от земли вместе с троном, сжигая всё вокруг в ярости. Огонь воскрешает Фриде, и вместе они сражаются с игроком. Фриде значительно замедляется, изредка атакуя косой, и часто использует волны льда, а также исцеляет Арианделя. В свою очередь, Ариандель бьёт по полу чашей и выливает из неё жидкий огонь.</p>
            <h3>Третья фаза</h3>
            <p>После победы над парным боссом Фриде воскресает в качестве Чёрного пламени Фриде. В дополнение к предыдущим атакам, она достаёт маленькую ледяную косу, использует мощь тёмного пламени и становится ещё быстрее и сильнее, чем раньше.</p>
