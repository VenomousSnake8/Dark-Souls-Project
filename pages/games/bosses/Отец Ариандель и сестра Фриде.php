                <p>Бой протекает разом с двумя врагами. Но, ввиду их количества, Фриде ведёт себя не так агрессивно, как раньше. Поэтому можете смело игнорировать её атаки и лечение босса; сосредоточьте все ваши атаки на Отце Арианделе, к тому же, он получает повышенный урон. Если у вас есть пиромантия <a href="#">Большая огненная сфера хаоса</a>, то она очень поможет в бою с ним.</p>
                <p>Фриде же улучшает свои ледяные атаки и периодически нападает на вас и лечит босса. Легче её игнорировать и держаться ближе к Арианделю. Однако, пока игрок занят Арианделем, она может незаметно подойти (её не будет видно за жирной тушей Арианделя) и провести какую‐нибудь атаку. Лучше держать Фриде и Арианделя на расстоянии друг от друга. На данной фазе, кроме всего прочего, очень хорошо помогают заряженные атаки по Арианделю.</p>
            </div>
            <div id="cat3-divt" style="display: none;">
                <p>Ариандель погибает, но успевает ещё раз воскресить Фриде и передать ей всю силу своего огня. Не стоит атаковать её, пока она не сделает «взрыв тьмы» в начале боя, полностью зарядившись, иначе вы попросту потеряете заряды. Также не стойте близко к ней в начале боя всё по тем же причинам.</p>
                <p>Здесь Эльфриде приобретает ужасающую скорость атак и чудовищные комбинации смерти. Лучшим выходом будет держаться на среднем расстоянии, кидая в неё какую‐нибудь магию.</p>
                <p>Если вы игрок ближнего боя, то всё то же самое, но атаковать её в перерывах между её атаками косами. Не стойте слишком близко, это достаточно быстро станет вашей погибелью. Помимо указанной выше магии, стоит внимательно следить за самой Фриде, так как <b>казнь</b>, являющаяся захватом, наносит огромный урон, но на неё можно среагировать: перед атакой дополнительная коса Фриде вспыхивает, босс немного приседает и почти сразу летит на вас. В этот момент лучше перекатиться на неё: в таком случае время нахождения рядом с боссом минимально, и шанс успешно уклониться значительно выше. Однако, стоит быть осторожным, так как похожим образом начинается ледяной конус, от которого перекатываться нужно вбок, а не вперёд. Внимательно следите за движениями Сестры: атаки можно отличать и реагировать соответственно.</p>
                <p>Так же, как и во время первой фазы, можно опять-таки ловить Фриде на удар в спину с последующей заряженной силовой атакой, на этот раз окна будут после проведения атак с приземлением или казни (либо же перед зарядкой казни).</p>
            </div>
