            <ul>
                <li>После убийства босса <a href="#">служанка</a> в <a href="../locations/Храм огня.php">Храме Огня</a> начинает продавать <a href="#">сет посвящённой</a>.</li>
                <li>Во время первой кат-сцены перед боем можно заметить, что Фриде шевелит губами, когда говорит, благодаря чему она формально третий, после <a href="#">Андрэ</a> и <a href="Лотрик и Лориан.php">Лотрика</a> (младшего из принцев‐близнецов), персонаж с анимацией рта при разговорах. Тем не менее, во время разговора с ней, когда она сидит на стуле, анимации рта у неё нет.</li>
                <li>Судя по внешнему виду (похожее лицо, перья на плечах, длинные когти), можно предположить, что Отец Ариандель — <a href="#">Демон‐ворон</a>. Также, возможно, это объясняет нахождение нескольких молящихся демонов‐воронов перед церковью. Скорее всего, среди них он и получил титул Отца.</li>
                <li>Вероятно, именно Сестра Фриде, появившаяся в картине, оказала влияние на молодого чародея <a href="Понтифик Саливан.php">Саливана</a>, на что указывают схожие мотивы; научила обращаться с двумя клинками, так как в описании <a href="#">косы</a> говорится о том, что Фриде первоначально орудовала двумя мечами, а комбо из 5 ударов понтифика сильно напоминает комбо Фриде в 3 стадии.</li>
                <li>Вы можете атаковать Арианделя в первой фазе, но это будет бесполезно, так как вашим активным врагом в первой фазе является именно Фриде.</li>
                <li>Во второй фазе правая и левая стены разрушаются, тем самым арена битвы становится шире. Если на второй или третьей стадии боя вы погибли в области за стенами, то ваши души появятся рядом со стеной.</li>
