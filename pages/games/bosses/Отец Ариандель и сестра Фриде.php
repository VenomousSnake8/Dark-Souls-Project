                <p>Отец обладает несколькими атаками. Все они медленные, но наносят колоссальный урон, а для уклонения необходимо привыкнуть к его мувсету:</p>
                <ul>
                    <li><b>Ползание</b>: босс довольно резво ползёт к вам, четыре раза ударяя чашей по пути. Под чашей в это время крайне не рекомендуется стоять. Обычно с этой атаки начинается вторая фаза, хотя босс может её исполнять произвольно, чаще тогда, когда игрок на дальней дистанции.</li>
                    <li><b>Плевки огнём</b>: если вы попадёте под удар, это гарантированная смерть! Поэтому вам стоит либо далеко отбежать, либо держаться вплотную. Если вы увидели, что уже не успеете сделать что‐либо из перечисленного, начинайте описывать большой круг по арене.</li>
