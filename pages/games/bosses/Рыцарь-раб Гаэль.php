            <h2>Тактика</h2>
            <ul>
                <li>Холмистый рельеф арены можно разнообразно использовать для получения преимущества в бою, так как большинство атак Гаэля будут уходить в землю или пролетать над игроком.</li>
                <li>Сражаясь в ближнем бою, лучше уходить от атак перекатами, если в наличии нет щита с высокой стабильностью, и стараться держаться у него за спиной. Во время пауз между атаками можно ошеломить босса мощными или обычными ударами двуручного оружия, причём, при достаточной выносливости, несколько раз подряд. При использовании двуручного меча в двуручном хвате босс оглушается каждый третий удар.</li>
                <li>Уязвим к статусным эффектам (<a href="#">кровотечению</a>, <a href="#">обморожению</a>, <a href="#">ядам и токсинам</a>), и если от кровотечения босс получает совсем немного урона, то яд/токсин/обморожение могут быть основой для убийства босса, в связи с чем он чрезвычайно уязвим к навыку <a href="#">посоха сказителя</a>. Отравить не очень просто, зато урон в первой фазе составляет 36 за тик (при 10 <a href="#">удачи</a> на <a href="#">NG+6</a> примерно 3700 урона), во второй фазе 96 за тик (примерно 8500 урона).</li>
                <li>После первой фазы приобретает солидную защиту от тьмы, остальные сопротивления остаются на средне-высоком уровне.</li>
                <li>Во второй и третьей фазе он получает больше урона от <a href="#">меча убийцы нежити</a>, как если бы он опустошился.</li>
                <li>Если во второй и третьей фазе оглушить Гаэля ударом в голову, то ему можно будет сделать <a href="#">рипост</a>. Однако, окно для рипоста очень маленькое, поэтому легче всего делать это с дружественным фантомом.</li>
