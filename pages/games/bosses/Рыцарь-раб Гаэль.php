                    <li>Отлетает в сторону, а затем резко меняет направление в воздухе, устремляясь к негорящему, с последующим горизонтальным ударом мечом и плащом.</li>
                    <li>Удар сверху вниз, схожий с ударом второй фазы, но теперь вырывание меча из земли происходит с последующим разворотом вокруг себя или же с применением "Волчьего меча".</li>
