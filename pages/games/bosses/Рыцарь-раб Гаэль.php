            <h2>Бой</h2>
            <p>В бою можно выделить три фазы:</p>
            <ul>
                <li>Гаэль ползает на четвереньках, размахивая мечом. Его движения и атаки достаточно стремительны. Он с большой скоростью то разрывает, то сокращает дистанцию, а порой взмывает в воздух для нанесения сокрушительного удара. Урон в этой фазе достаточно средний, но зато практически все атаки идут сериями, некоторые удары весьма неожиданны и молниеносны. Но после серии ударов следуют достаточно большие окна для ответных действий.</li>
                <li>Пролитая кровь Гаэля, ставшая кровью тёмной души, придаёт ему сил. Он встаёт и выпрямляется в полный рост, а вокруг него и при его атаках появляется красноватая дымка, в которой также можно разглядеть очертания человеческих черепов. Его атаки полностью меняются, становясь куда более размеренными, но при этом наносят больше урона. Также босс начинает использовать свой арбалет и магию. Его плащ развевается, как 2 больших крыла, которые он также использует для атаки.</li>
                <li>Потеряв много крови, Гаэль переполняется мощью тёмной души. Переходу в эту стадию предшествует крик боли, после чего из тела рыцаря-раба исторгается множество кровавых черепов, разлетающихся по округе. Небеса становятся серыми, в землю бьют белые молнии. В этой стадии его атаки наиболее стремительны и обладают огромным уроном. Окна для ответных ударов становятся совсем небольшими. Может телепортироваться за спину игрока.</li>
            </ul>
