                <ul>
                    <li><b>Двойной взмах</b> — бьёт мечом из стороны в сторону.</li>
                    <li><b>Удар в падении</b> — прыгает вверх, разворачивая меч лезвием вниз, а затем падает, нанося удар. Часто использует как последний удар после различных серий. Также есть другая анимация: при падении он не вонзает меч в землю, а ударяет лезвием, держа меч одной рукой. Начинать уворачиваться следует после того, как меч Гаэля будет смотреть лезвием вниз.</li>
                    <li>Кричит, а затем с яростным рёвом наносит 4 рубящих удара, после которых проводит "удар в падении". Эта атака может стать проблемой, если у вас осталось мало <a href="#">выносливости</a>.</li>
                    <li><b>Удар с разворота</b> — совершает пол-оборота с последующим диагональным ударом сверху вниз.</li>
                    <li>Если вы стоите за спиной босса, то он ударит одиночной атакой с небольшой задержкой с разворота в вашу сторону.</li>
                    <li><b>Пронзающий рывок</b> — устремляется вперёд с последующим колющим ударом с большого расстояния. Меч Гаэля при этом покрывается красноватым свечением. Уклониться очень легко, но если он всё же попадает — то нанизывает негорящего на меч, а затем отшвыривает в сторону на очень большое расстояние.</li>
                    <li><b>Рывок</b> — ещё одна атака с резким сокращением дистанции. При этом Гаэль совершает диагональный удар снизу вверх. За этой атакой часто следует удар с разворота.</li>
                    <li>Также в этой фазе Гаэль стремительно скачет вокруг нас, то разрывая, то сокращая дистанцию, комбинируя свои атаки.</li>
                </ul>
