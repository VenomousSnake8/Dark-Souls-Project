                <ul>
                    <li>Мощный удар мечом сверху вниз, вгоняющий его в землю, после чего босс резко вырывает его. Движение напоминает оружейный навык <a href="#">Сотрясателя камней</a>. Урон достаточно большой.</li>
                    <li><a href="#">Нимб Белого Пути</a> — заряжает и выпускает множество "Нимбов", имеющих увеличенный размер, по сравнению с доступным игроку заклинанием, которые вскоре возвращаются по такой же траектории, по которой летели. Каст можно сбить. Достаточно просто увернуться, если находиться между двумя нимбами.</li>
                    <li><b>Скоростная стрельба</b> — достаёт свой арбалет и с огромной скоростью выпускает множество болтов. Есть три вида доставания арбалета: с отходом Гаэля назад; стоя на месте; после обычной горизонтальной атаки мечом вместо второй резко достаёт арбалет и, стоя на месте, стреляет. Урон от единичного попадания невелик. Эта атака часто следует сразу за "нимбами". Эту атаку также можно прервать.</li>
                    <li><b>Круговой удар</b> — замахивается горизонтальным ударом, а затем бьёт вокруг себя, также добавляя удары плащом.</li>
                    <li>Колющий удар вперёд сверху вниз с небольшим замахом, также сопровождается ударом плаща. От этой атаки нужно уходить либо влево, либо вправо — если перекатиться в босса или от него, плащ нанесёт урон.</li>
                    <li>Серия из двух рубящих ударов по горизонтали с завершающим колющим ударом. И снова удары плащом. Если вы стоите за ним, то он ударит точно такой же горизонтальной атакой, только за спину с разворотом.</li>
