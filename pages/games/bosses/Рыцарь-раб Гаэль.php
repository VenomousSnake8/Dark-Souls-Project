            <h2>Описание</h2>
            <p>Верный слуга <a href="#">Девушки-Художницы</a>, побывавший в бесчисленных битвах, долгие годы был занят поисками <a href="#">негорящего</a>, дабы, оказавшись в <a href="#">картине</a>, тот сжёг её дотла, а его госпожа увидела бы огонь.</p>
            <p>Выполнив свою цель, Гаэль отправляется в <a href="#">Город за стеной</a>, дабы там найти последний пигмент для новой картины &mdash; <a href="#">кровь тёмной души</a>, принадлежащей владыкам пигмеев. Но это путешествие становится для него последним.</p>
