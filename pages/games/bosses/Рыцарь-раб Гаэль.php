                    <li><b>Круговой удар</b> — берёт меч в две руки, некоторое время замахивается, а затем бьёт горизонтальным ударом вокруг себя. Сопровождается взмахом плащом и черепками.</li>
                    <li><b>Рывок</b> — резко сокращает расстояние с последующим ударом сверху вниз.</li>
                    <li><b>Телепортация</b> — исчезает, а затем появляется у негорящего за спиной. При этом сбивается захват цели. После телепорта обычно выполняет круговую атаку.</li>
