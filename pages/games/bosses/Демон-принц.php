            <h2>Бой</h2>
            <h3>Демон в агонии и Демон из глубин</h3>
            <p>В первой фазе игроку предстоит сражаться сразу с двумя крылатыми демонами. Почти сразу после начала боя Демон из глубин загорится и начнёт активно идти на сближение с игроком. В свою же очередь, Демон в агонии произведёт две атаки токсичным взрывающимся лучом, после загорится и тоже начнёт преследовать игрока.</p>
            <p>В ходе боя демоны меняются друг с другом местами: один загорается и начинает быстро сокращать дистанцию до игрока, потухший — держится вдалеке и атакует токсином.</p>
            <p>Из-за небольшой погрешности во времени между сменой режимов атак оба демона могут либо загореться, либо потухнуть. В любом случае, один из них обязательно погаснет сразу после удара обеими лапами по земле (сопровождается взрывом на небольшой дистанции), а другой — загорится с характерным визгом.</p>
            <h3>Демон-принц</h3>
            <p>Вторая фаза. После того, как оба демона падут, в последнем умершем воплотится Демон-принц. Его магические атаки будут зависеть от того, в ком именно он воплощён. В то время, пока он поднимается и расправляет крылья, есть достаточно времени для лечения, нанесения мазей, смол и т.д.</p>
            <p>Все атаки из первой стадии сохраняются, но наносят больше урона, зачастую сопровождаются взрывом или одним лишним ударом в конце.</p>
            <p>Во второй фазе у босса возрастают все сопротивления — лучше взять оружие с хорошим уроном, поскольку закаливать и мазать оружие времени почти не будет, а бой будет очень долгим, поскольку сопротивления к урону у него больше, чем у обоих демонов из первой фазы вместе взятых.</p>
            <h2>Атаки</h2>
            <h3>Горящий демон</h3>
            <ul>
                <li><b>Сближение</b>: наносит четыре последовательные атаки лапами по земле в движении, после небольшой задержки бьёт в пятый раз, затем этой же рукой делает скоблящий удар по дуге перед собой.</li>
                <li><b>Апперкот</b>: правой рукой упирается в землю и после наносит удар снизу вверх.</li>
                <li><b>Удар</b>: один-два взмаха руками справа налево и слева направо. После них может идти апперкот.</li>
                <li><b>Прижимание</b>: вскидывает левую руку вверх, встаёт на задние лапы и тут же падает, прижимая игрока рукой к земле.</li>
                <li><b>Удар обеими лапами</b>: наносит мощный удар обеими лапами перед собой, занося их вверх.</li>
                <li><b>Взлёт</b>: подпрыгивает вверх и летит в сторону игрока с прижимающей атакой. Используется только один раз за битву Демоном из глубин, сразу после начала сражения.</li>
                <li><b>Удар обеими лапами со взрывом</b>: слегка прижимается к земле, после заносит горящие лапы вверх и тут же бьёт перед собой со взрывом. После этой атаки Демон истощён и открыт для пяти-шести ударов.</li>
            </ul>
            <h3>Потухший демон</h3>
            <ul>
                <li><b>Дыхание</b>: начинает дышать справа налево токсичными облаками.</li>
                <li><b>Луч</b>: пускает по земле длинный луч из фиолетовых частиц, после небольшой задержки по этой же траектории проносится несколько токсичных взрывов. Легко уклониться.</li>
                <li><b>Плевок</b>: изрыгает три плевка токсичной рвотой.</li>
                <li><b>Удар</b>: одиночный слабый удар.</li>
            </ul>
            <h3>Демон-принц</h3>
            <ul>
                <li><b>Возрождение</b>: в тот же момент, когда отрастают крылья, область вокруг Демона взрывается и откидывает игрока назад.</li>
                <li><b>Сфера</b>: чаще всего используется после <b>возрождения</b>. Взмывает вверх и назад, кидая большую огненную сферу.</li>
                <li><b>Рывок</b>: взлетает и делает рывок вперёд, оставляя огненный след.</li>
                <li><a href="#"><b>Бурлящий хаос</b></a>: взлетев, кидает три сгустка бурлящего хаоса, которые, если не задевают игрока, остаются на земле и взрываются спустя несколько секунд.</li>
                <li><a href="#"><b>Плывущий хаос</b></a> (опционально): создаёт две сферы, обстреливающие игрока огнём. Используется, если последним убитым демоном был Демон в агонии.</li>
                <li><b>Захват</b>: может схватить игрока лапой и бросить его об землю, нанося приличный урон. На высоких <a href="#">NG+</a> атака может стать фатальной, поэтому стоит внимательно следить, что делает босс.</li>
                <li><b>Луч</b> (опционально): отлетает далеко назад, отбрасывая игрока, встаёт в стойку и атакует лучом по зигзагу, три раза меняя дальность и скорость. Первый луч — коротко и быстро, второй — дальше и медленнее, третий — почти прямо и очень медленно. После этих атак следует характерный крик и мощный удар лучом по прямой, сбивающий с ног и наносящий много урона (изредка смертельный). Во время каста последнего луча вокруг Демона образуется огненная аура, наносящая урон и отбрасывающая игрока, потому придётся отбегать. Использует только в том случае, если Демон-принц воплотился в Демоне из глубин. Отбрасывания в самом начале атаки можно избежать, сделав вовремя перекат, что немного упростит сближение с боссом.</li>
                <li><b>Шар</b> (опционально): отлетает назад и начинает долго создавать большую огненную сферу над собой. Затем сфера раскалывается на большое количество огненных сгустков, разлетающихся в стороны. Имеет большой урон. Как правило, использует Шар сразу после <b>Плывущего хаоса</b>. Атака не достаёт прямо под боссом, если стоять вплотную к его правой руке. Также атаку можно частично сбить при нанесении достаточного урона (лучше всего подойдут заряженные атаки): демон пошатнётся и прекратит извергать пламя, недоделанная сфера разлетится на сравнительно малое количество сгустков, а урон от них будет снижен. Использует только в том случае, если Демон-принц воплотился в Демоне в агонии.</li>
            </ul>
            <h2>Тактика</h2>
            <ul>
                <li>Эффективнее всего наносить урон, находясь у него под ногами, так он не будет наносить урон и прерывать атаку как в первой, так и во второй фазе.</li>
                <li>Все демоны имеют уязвимость к рубящему урону сравнительно колющего и дробящего.</li>
                <li>Лучше всего подходит оружие с хорошим сочетанием скорости атаки и урона по <a href="#">балансу</a> — Большие мечи и Косы. Также можно пользоваться Гигантскими мечами или Большими молотами, но низкая скорость атак подвергает большему риску не успеть увернуться после нанесения удара, что с лихвой компенсируется возможностью проводить рипосты после каждых &approx;5-6 ударов (для этого сгодится, например, <a href="#">Зуб дракона</a>).</li>
                <li>Хорошо подходит оружие, имеющее врождённый урон магией — например, <a href="#">Меч правосудия</a> или <a href="#">Меч лунного света</a>.</li>
                <li>Чтобы сэкономить <a href="#">фляги с эстусом</a>, можно пользоваться заклинаниями лечения в перерывах между атаками, но делать это следует крайне осторожно — оба демона в первой фазе склонны к очень быстрому сокращению дистанции, а их манера ведения боя крайне агрессивна!</li>
                <li>Оружие чёрных рыцарей будет наносить дополнительный урон по понятным причинам — босс является демоном.</li>
                <li>Разумно будет использовать сет с большой защитой от огня.</li>
                <li>Лучшим выбором для убийства босса станет <a href="#">меч чёрного рыцаря</a> — он сочетает в себе высокую скорость атак, лучший физический урон среди двуручных мечей в игре и бонус к урону по демонам в сочетании с полезным навыком, позволяющим наносить продолжительные серии ударов. К тому же, вес его относительно мал. Другое оружие чёрных рыцарей тоже уместно, но по эффективности оно значительно хуже.</li>
            </ul>
