            <h2>Примечания</h2>
            <ul>
                <li>Следуя из описания <a href="#">Меча Лориана</a> и нового описания <a href="#">души босса</a> можно предположить, что возрождённый во второй фазе Демон-принц — это тот самый демон, которого Лориан сразил до проклятия.</li>
                <li>В первой фазе в самом начале боя возможно подвести одного из демонов к месту падения союзника, что ненадолго оглушит демона и позволит сделать рипост.</li>
                <li>Демоны отличаются по цвету огня на коже (когда они горят): у Демона в агонии — красный, у Демона из Глубин — оранжевый. Цвет Демона-Принца будет таким же, как и у последнего убитого демона.</li>
                <li>Изначально задумывалось, что бой начнётся с одним демоном, но после снятия половины жизни на поле боя должен был появиться второй, однако идея не добралась до финальной версии.</li>
                <li>Должен был называться <i>Багряной летучей мышью</i> (англ. <i>Crimson Bat</i>) и изначально должен был быть в локации <a href="#">Могила Богов</a>. Впоследствии локация была вырезана из игры, а сам босс должен был после этого находиться в <a href="#">Тлеющем озере</a>.</li>
                <li>Имеет <a href="https://www.youtube.com/watch?v=YocgP4HJsUs">вырезанный саундтрек</a>, ID которого и отсылает к Могиле Богов.</li>
