            <h2>Описание</h2>
            <h3>Первая фаза</h3>
            <p>Босс представляет собой большую группу <a href="#">дьяконов</a> трёх типов, не спеша преследующих игрока. При смерти дьяконов им на смену немедленно приходят новые, так что просто убить их всех невозможно. Дьяконы наносят очень мало физического урона и иногда колдуют такие же слабые огненные шары своими посохами. Толстые дьяконы могут использовать <a href="#">Силу</a>.</p>
            <p>В одного из дьяконов прилетает красная сфера, и только его убийство нанесет урон боссу в целом. После его смерти сфера перелетает в другого случайно выбранного дьякона.</p>
            <h3>Вторая фаза</h3>
            <p>На 50% жизней босса (примерно 5-7 убитых дьяконов) появится архидьякон Ройс, перманентно получающий красную сферу. Ройс вооружён <a href="#">Большим посохом архидьякона</a> и весьма часто применяет шар тьмы, который наносит существенный урон и проходит сквозь препятствия. Кроме того, появляются два толстых синих дьякона, которые могут лечить всех находящихся вокруг. Каст шара тьмы можно прервать, оглушив Ройса или помогающих ему дьяконов, но если это не получается сделать из-за скучковавшихся вокруг Ройса дьяконов, следует приготовиться увернуться от заклинания.</p>
            <p>Время от времени дьяконы начинают произносить другое коллективное заклинание: арена темнеет, и у игрока начинает заполняться полоса проклятия. Можно прервать заклинание, убив или просто ударив одного из прислужников, направляющих посох вверх.</p>
            <h2>Атаки</h2>
            <h3>Первая фаза</h3>
            <p>По факту это просто сражение с рядовыми противниками, а точнее с <a href="#">дьяконами</a>.</p>
            <ul>
                <li>Дьяконы в основном атакуют вас <a href="#">огненными шарами</a>.</li>
                <li>Вблизи начинают использовать свои посохи.</li>
                <li>Толстые дьяконы — самые опасные. Помимо огненных шаров, они также используют <a href="#">Силу</a> и могут легко сбить персонажа с ног. К тому же они очень «толстые» в плане здоровья и имеют повышенный баланс.</li>
            </ul>
            <h3>Вторая фаза</h3>
            <p>После снятия примерно половины здоровья появляется сам босс в лице архидьякона Ройса. К атакам дьяконов добавляется пара новых, сам же босс довольно пассивен (в его собственном арсенале есть лишь одна атака). Также дьяконы будут стараться держаться вместе, обороняя Ройса.</p>
            <ul>
                <li><b>Сфера тьмы</b>. Дьяконы начинают кастовать коллективное заклинание, во время которого прямо над Ройсом появляется большая сфера, которая затем летит в игрока. Сам снаряд игнорирует любые препятствия на пути. Урон огромный.</li>
                <li><b><a href="#">Проклятие</a></b>. Самая опасная атака. Как и при сфере тьмы, маги кастуют коллективное заклинание. В это время верхняя часть комнаты начинает заполняться огромным облаком тьмы, которое накладывает на игрока эффект проклятия и, скорее всего, мгновенно убивает при заполнении шкалы.</li>
                <li><b>Удар посохом</b>. Если подойти вплотную к Ройсу, тот может ударить игрока посохом. Наносит существенный урон.</li>
                <li><b>Лечение</b>. Дьяконы в синих мантиях могут изредка лечить босса.</li>
            </ul>
            <h2>Тактика</h2>
            <ul>
                <li>Поскольку дьяконы хрупки и не имеют баланса, босс становится легче с применением оружия с размашистыми атаками и другого АоЕ. Для оружия длина при этом желательна, но не обязательна.</li>
                <li>Толстые дьяконы, в отличие от остальных, имеют большее сопротивление к дробящему урону, что компенсируется их же повышенной уязвимостью к колющему.</li>
                <li>Обладают огромным сопротивлением к магическому урону и тьме. К тому же, у игрока на этой стадии игры вряд ли есть мощные заклинания, наносящие урон по площади, поэтому рекомендуется использовать оружие ближнего боя.</li>
                <li>Поскольку дьяконы часто умирают, будут полезны предметы с эффектом при убийстве, например <a href="#">Асторское копье</a> и/или <a href="#">Кольцо злого глаза</a> (проверено как раздельно, так и оба этих предмета). Также от убийства может сработать восстановление <a href="#">эстуса</a>.</li>
                <li>Поскольку целей много, будут полезны предметы с эффектом при ударе, например, <a href="#">Нож мясника</a>.
                    <ul type="disc">
                        <li>Пример: при первом прохождении босса довольно легко победить связкой <a href="#">Асторского двуручного меча</a> и кольца <a href="#">Левый глаз понтифика</a>. При атаке плотной группы врагов эффект кольца будет срабатывать каждый удар.</li>
                    </ul>
                </li>
                <li>При высоких параметрах веры очень эффективно заклинание <a href="#">Буря молний</a> в связке с <a href="#">Талисманом святого</a>. Также неплохими решениями могут стать использование против дьяконов чуда <a href="#">Гнев богов</a> или даже навык <a href="#">священного меча Вольнира</a>.</li>
                <li>Можно пользоваться гробом, чтобы безопаснее подлечиться.</li>
                <li>На рядовых дьяконов действует предмет <a href="#">Красивый череп</a> и пиромантия <a href="#">Гармония</a>. На самого Ройса, в свою очередь, не действует ничего из этого.</li>
                <li>Против самого Ройса очень эффективно заклинание <a href="#">Ядовитые миазмы</a>. Архидьякон почти не двигается, из-за чего заклинание будет наносить ему существенный урон, а ввиду того, что оно обладает AoE эффектом, применить его можно даже когда Ройс находится за толпой дьяконов. Помимо урона по архидьякону, заклинание может убить толстых синих дьяконов, лишая босса возможности восстановить здоровье (лишь на время, но, как правило, можно убить Ройса ещё до того, как новые дьяконы придут).</li>
            </ul>
