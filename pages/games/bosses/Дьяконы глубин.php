            <h2>История</h2>
            <p>Культ «Дьяконы глубин» был основан в Храме глубин. Основатели были клириками, которые веровали в Пламя. Руководят культом архидьяконы, приближенные к <a href="Олдрик, пожиратель богов.php">Олдрику</a>. Точно неизвестно, сколько было до нынешнего времени архидьяконов, но на данный момент их трое: <a href="#">МакДоннелл</a>, Ройс и <a href="#">Климт</a>.</p>
            <p>Неизвестно, когда они стали еретиками и начали отрекаться от своего изначального верования, но точно известно, что этому поспособствовал Олдрик.</p>
            <p>На данный момент Дьяконами глубин руководит архидьякон Ройс. Климт ушёл служить к <a href="#">Розарии</a>, потому что считал её богиней, а МакДоннелл вместе с Олдриком ушёл в <a href="#">Иритилл</a>. Там он основал <a href="#">ковенант</a>, чтобы его члены хранили сон его господина. Но перед уходом МакДоннелл обучил Ройса и его группу дьяконов заклинанию <a href="#">Глубинная душа</a>, чтобы они могли эффективно защищаться.</p>
            <p>Ройс остался в Храме глубин, чтобы защищать усыпальницу Олдрика, в надежде, что тот когда-нибудь вернётся обратно.</p>
