            <h2>Примечания</h2>
            <ul>
                <li>Один из немногих боссов в серии Dark Souls, который говорит с вами.</li>
                <li>В сцене перед боем Оцейрос ведёт себя так, как будто у него в руках находится младенец, и в первой фазе боя держит левую руку так, будто в ней что-то есть; во второй фазе он обнаруживает, что младенца нет, и начинает выкрикивать «Оцелотт, где же ты?». Это можно было бы списать на безумие Оцейроса, но во время боя действительно слышен детский плач. В файлах игры можно обнаружить <a href="https://www.youtube.com/watch?v=P6vCZdcp6hE">вырезанную аудиодорожку</a> из битвы с боссом; она включает в себя не только плач и смех младенца, но затем следует ряд звуков, похожих на дробление и раздавливание чего-то. Кроме того, в файлах игры <a href="https://imgur.com/a/asN7Z">присутствуют текстуры</a> для модели младенца-Полого, и названия соответствующих файлов "c2090_baby_a".dds и "c2090_babydead_a".dds содержат c2090 — обозначение Оцейроса. Таким образом, можно предположить, что в первоначальной версии игры у Оцейроса в руках действительно был младенец, а бой включал в себя гибель Оцелотта — при переходе от первой фазы ко второй слепой Оцейрос роняет дитя и сам раздавливает его посохом. Не исключено, что Оцелотт мог быть отдельной целью для атак на модели босса. Вероятно, младенец был вырезан из цензурных соображений.</li>
                <li>Его нынешняя версия должна была в качестве босса под совершенно иным именем находиться в вырезанной локации <a href="#">Могила Богов</a>.</li>
                <li>В коде игры прописан как Dragon Angel.</li>
            </ul>
