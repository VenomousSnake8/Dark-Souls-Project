            <h2>Атаки</h2>
            <h3>Первая фаза</h3>
            <ul>
                <li><b>Удар посохом по земле</b>. Медленная, но сильная атака.</li>
                <li>Размашистые атаки посохом перед собой.</li>
                <li><b>Облако</b>. Вонзает посох в землю, после чего перед ним образуется облако, похожее на чары <a href="#">Ядовитые миазмы</a> (наносит урон, пока вы находитесь в нем).</li>
                <li><b>Удар хвостом</b>. Если находиться сзади, атакует хвостом.</li>
                <li><b>Атака с прыжка</b>. Нахождение вблизи может спровоцировать на взлёт и удар под себя, который наносит крайне высокий урон и который невозможно заблокировать, если стоять непосредственно под боссом.</li>
            </ul>
            <h3>Вторая фаза</h3>
            <ul>
                <li><b>Комбо</b>. Размашистые атаки лапами и укусы. Не достают под боссом, наносят средний урон, легко заблокировать.</li>
                <li><b>Взлёт</b>. Взлетает и пролетает над игроком, изрыгая кристаллы. Наносит ощутимый урон и заполняет шкалу проклятия.</li>
                <li><b>Наскок</b>. Прыгает на игрока, нанося урон в точках приземления (возле лап), после чего сразу следует укус перед собой.</li>
                <li><b>Рывок</b>. Практически мгновенная атака с очень высоким уроном, часто применяемая после других атак и задевающая под боссом, из-за чего нахождение в этой недосягаемой для других атак зоне может быть очень опасным.</li>
                <li><b>Разбег</b>. Босс начинает рычать и быстро бежать вперёд, размахивая передними лапами и пастью. Опасная атака, которая наносит большой урон, но уклониться от нее вполне возможно. Применяется обычно на дальней и средней дистанциях, но иногда и вблизи. Имейте в виду, что в это время босс наносит урон всем своим телом, включая ноги, и может задеть вас даже под собой!</li>
            </ul>
            <h2>Тактика</h2>
            <h3>Общее</h3>
            <ul>
                <li>Босс уязвим к молниям, кровотечению и обморожению, но имеет сопротивление к магии и иммунитет к яду и токсинам.</li>
                <li>Его удары во время перехода во вторую стадию, которые сопровождаются небольшими магическими взрывами, отнимают очки концентрации при попадании.</li>
                <li>Если по вам попадает атака, которая с виду кажется ледяной, заполняется шкала проклятия.</li>
                <li>Арена для битвы весьма неудобна, и если вы выбрали дальний бой как тактику, босс периодически будет вас зажимать. Стоит это продумать.</li>
                <li>Возле будущего выхода с арены есть толстые корни, образующие подобие стены. Если заманить туда босса, а самому оказаться возле выхода с арены, то Оцейрос не сможет покинуть эту ловушку. Он будет постоянно прыгать на вас, но стена не даст ударам достичь цели. В это время его можно расстрелять. Стоит отметить, что провести эту тактику крайне легко.</li>
                <li>Хвост босса достаточно длинный, к тому же по нему также проходит урон. Это можно использовать, чтобы избежать атаки <b>облако</b> и нанести боссу урон.</li>
            </ul>
            <h3>Первая фаза</h3>
            <ul>
                <li>Оптимальное место — сбоку от босса или сзади в упор. Всё время обходите босса, нанося удары. Можно стоять сзади, но удары хвостом иногда обходят блок (впрочем, если стоите в упор, эти удары, как и любые другие в этом положении, по вам вообще не попадут), а босс начинает спамить атакой с прыжком.</li>
                <li>Если вы маг, отходите от босса, выжидая момент для каста заклинания.</li>
                <li>У босса есть две точки для взятия в цель — грудь и таз. На первой фазе сражающимся в ближнем бою будет легче попадать, если целиться в таз, в дальнем — в грудь. На второй фазе это не имеет большого значения, однако, если всё-таки осмелитесь держаться под ним, продолжайте целиться в таз.</li>
            </ul>
            <h3>Вторая фаза</h3>
            <ul>
                <li>Важный момент. Бой с боссом в этой фазе проходит довольно своеобразно: он носится по всей арене, попутно размахивая лапами и пастью, что означает, что вы не сможете всё время находиться рядом с боссом и будете вынуждены бегать за ним или ждать, пока он подбежит сам.</li>
                <li>Находиться под боссом небезопасно из-за того, что рывок достанет вас там (если здоровье прокачано не очень хорошо, скорее всего убьёт). Стоит понимать, что реагировать на рывок невозможно, но можно предугадывать, когда он его сделает: босс старается применить эту атаку, когда персонаж находится под ним, следовательно, после любой его атаки нахождение под ним спровоцирует его на рывок. <b>Важно:</b> после некоторых атак рывок применяется сразу же, как часть комбо.</li>
                <li>Впрочем, под боссом можно стоять некоторое время, к примеру, после уклонения от какой-либо атаки, нанося удары, однако стоит сразу выходить оттуда, пройдя между ног. Это позволит избежать урона от рывка, а если босс его не сделает, вы окажетесь в выгодном положении: позади босса, близко к нему. В таком положении ни одна атака вас не заденет, а босс сделает круговой удар хвостом, который тоже пройдёт у вас над головой, позволяя нанести пару ударов/восстановить выносливость.</li>
                <li>Также можно стоять сбоку от босса, что позволит вам выйти из зоны поражения сразу, как только заметите разбег.</li>
                <li>Легко убивается быстрым оружием (хорошо подходит <a href="#">меч тьмы</a>), зачарованным или закалённым на молнию, в сочетании с правым глазом понтифика. Интервалы между атаками у босса достаточно большие, чтобы активировать эффект кольца (в идеале он не прекратит своё действие в течение всего боя).</li>
                <li>Для тяжеловесов босс не слишком сложный: берем <a href="#">Большой щит Славы</a> (помимо 40 силы, хорошо бы иметь &approx;30 выносливости). Блокируем любую атаку, после блокирования наносим 1-2 удара. Проблемы могут доставить только изрыгания кристаллов, но от них легко уклониться.</li>
            </ul>
