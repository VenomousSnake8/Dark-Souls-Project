            <h2>Тактика</h2>
            <h3><i><u>Общее:</u></i></h3>
            <ul>
                <li>Босс имеет высокую защиту от огня. Иммунитет к <a href="#">яду и токсинам</a>.</li>
                <li>Ни одна его атака не <a href="#">парируется</a>, а сам он никогда не открывается для критической атаки.</li>
                <li>Уязвим к молнии, тьме и <a href="#">кровотечению</a>.</li>
                <li>Слабо уязвим к <a href="#">обморожению</a>.</li>
                <li>Тщательно выбирайте моменты для лечения или атаки заклинаниями — босс может нанести удар аккурат во время вашей задержки. Более того, босс может побежать галопом за отступающим протагонистом, исключая лечение вообще.</li>
            </ul>
            <h3><i><u>Меч:</u></i></h3>
            <ul>
                <li>Эта вариация босса потребует либо хорошего навыка перекатов, либо щита с хорошей стабильностью и прокачанной выносливости (хватает &approx;120 с <a href="#">щитом Хавела</a>).</li>
                <li>Закрываетесь щитом, выжидая момента для атаки. Если предпочитаете технику уклонения, обходите босса против часовой стрелки, перекатываясь в ту же сторону. Может произойти такое, что босс будет просто пытаться к вам повернуться и не будет атаковать: это идеальный момент для пары ударов.</li>
                <li>Босса можно атаковать после завершения серий, после 2-3 следующих подряд усиленных огнём ударов и после удара по земле.</li>
                <li>У босса есть удар с продолжительной анимацией, перед атакой можно ударить даже медленным оружием, после чего успешно увернуться и ещё ударить. Главное — следить за <a href="#">выносливостью</a>.</li>
                <li>Если вы маг, можно использовать тактику отхода: всё время старайтесь увеличить дистанцию, используя заклинания после указанных выше атак. Однако босс часто будет переходить на бег, из-за чего сильно разорвать расстояние не получится. Кроме того, от многих атак проще увернуться перекатом вперед-направо, чем назад. Оптимальным вариантом будет комбинирование этих двух тактик.</li>
            </ul>
            <h3><i><u>Копьё и чудеса:</u></i></h3>
            <ul>
                <li>Держитесь в упор и обходите босса против часовой стрелки.</li>
                <li>От <a href="#">Гнева богов</a> уходите перекатом.</li>
                <li>Вблизи от его атак намного проще увернуться, а лечение он может и вовсе не использовать, если не отпускать босса от себя.</li>
                <li>Даже если вы маг, лучше взять оружие или использовать быстрые заклинания, так как важно всегда держаться как можно ближе к боссу, а он будет часто отходить от вас, используя некоторые атаки.</li>
            </ul>
            <h3><i><u>Сабля и пиромантия:</u></i></h3>
            <ul>
                <li>Лучше всего переждать, когда босс возьмёт другое оружие, защищаясь от ударов и уклоняясь от огненных снарядов.</li>
                <li>Когда босс сам начинает набирать расстояние, можно выпить эстус/нанести смолу, но он может взять посох где-то на другом конце арены, что может вызвать некоторые затруднения.</li>
                <li>Несмотря на то, что с этим оружием босс намного быстрее, вполне возможно, всё время отходя назад, закидывать его заклинаниями. Однако не стоит делать этого, когда он переходит на бег: он может увернуться от вашего снаряда, оказавшись вплотную, и сам нанести несколько ударов.</li>
            </ul>
            <h3><i><u>Посох и чары:</u></i></h3>
            <ul>
                <li>Сложность представляют его кристальные сгустки души, которые самостоятельно обновляются &approx;4 раз.</li>
                <li>Нужно всё время идти на сближение, а когда окажетесь рядом с боссом, берите оружие в 2 руки и начинайте обходить его. Наносите удары, как только появляется возможность. Это позволит вам оглушать босса, что прервёт анимацию возобновления сгустков.</li>
                <li>От всех атак можно уйти одним перекатом, если вы вплотную. На расстоянии от потока душ всё равно можно уйти, перекатившись вбок 2-4 раза.</li>
                <li>В этой вариации босс имеет довольно мало ближних атак, поэтому имеет смысл играть более напористо, дабы нанести как можно больше урона.</li>
            </ul>
            <h3>Вторая фаза</h3>
            <ul>
                <li>Всё время держитесь на максимальном расстоянии его размаха: это будет провоцировать босса на серию из 4 ударов со взрывом. После взрыва бегите и бейте босса (лучше в двуручном хвате) один раз, после чего опять набирайте дистанцию. Повторять до победы.</li>
                <li>Можно применить ту же тактику, что и с мечом: всё время обходите босса, атакуя в перерывах между его сериями. Когда увидите, что он начал серию из 4 ударов, бегите за спину боссу: так вы избежите урона и сможете сделать пару ударов по нему. От последнего взрыва можно просто закрыться щитом или отбежать. Это комбо можно и заблокировать (со <a href="#">щитом Чёрного Рыцаря</a> +5 и 160 выносливостью это возможно). При этом, даже если последняя атака в серии разобьёт щит, персонаж успеет накопить немного выносливости и поставить новый блок как раз перед взрывом. Можно также использовать <a href="#">Большой щит славы</a>, наложив <a href="#">чары</a> (даст 100% устойчивости) и о выносливости можно не беспокоиться. Можно использовать и другой щит, главное, чтобы его стабильность была не ниже 75.</li>
                <li>В этой фазе Лотрик сразу после проведения вертикальной Магической атаки Лорианом атакует магической атакой, поэтому не стоит лечиться сразу после ухода от атаки Лориана.</li>
                <li>Главная цель — убить Лотрика. Если его убить, то Лориан тоже умрет.</li>
            </ul>
