            <h2>Примечания</h2>
            <ul>
                <li>Перед первым заходом к боссу, он сидит перед <a href="#">костром</a> в позе <a href="#">Отдых</a>. При подходе к нему достает из костра <a href="#">витой меч</a> и сражается именно им.</li>
                <li>Является одним из немногих боссов, после убийства которого не появляется костер, которым можно воспользоваться по прямому назначению, так как взаимодействие с ним запустит финальную кат-сцену.</li>
