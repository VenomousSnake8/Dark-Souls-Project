                <li>Если пройти к боссу через туман, то он изначально, как и <a href="Понтифик Саливан.php">Понтифик Саливан</a>, не будет вас замечать, но бой всё же начнётся. Таким образом можно совершить последние подготовления уже на арене.</li>
