            <h2>Общие сведения</h2>
            <ul>
                <li>После входа в туман Судья Арго будет долго говорить о том, что королевская воля — это не забава, и начнёт взывать к копью Церкви. Пока он говорит, можно подбежать и бить его, как обычного <a href="#">гиганта-судью</a> (имеет чуть больше здоровья). Всё это время он не будет призывать бесплотных духов, а лишь атаковать руками вблизи, если подойти к нему (атаки наносят много физического урона). В конце своей речи перестанет атаковать и призовёт Копьё Церкви, после продолжит атаковать руками.</li>
                <li>Если не атаковать Арго во время его речи, то, призвав сначала фантома, а потом Копьё церкви, он исчезнет.</li>
                <li>Помимо босса на арену призывается враждебный фантом, одетый в <a href="#">сет стража картин</a> с <a href="#">саблей стражей картин</a>. Очень подвижный враг, который будет атаковать игрока серией быстрых атак, кидать кинжалы на дальних дистанциях, а также при низком количестве здоровья лечить себя чудом (а заодно и босса, если он будет поблизости). Когда у босса останется чуть больше половины здоровья — будет призван ещё один.</li>
                <li>Сам босс вооружён катаной и щитом, но щитом практически не прикрывается — в ближнем бою почти сразу начинает атаковать.</li>
            </ul>
            <h2>Атаки</h2>
            <p>Независимо от того, призван против вас игрок или нет, у босса будут следующие атаки:</p>
            <ul>
                <li>Автоматически вызывает сферы света над головой (похожи на <a href="#">наводящийся сгусток души</a>, но светлые), после эти сферы летят в игрока наподобие пучка стрел. Наносят урон молнией. Анимация атаки отсутствует.</li>
                <li>Использует <a href="#">фрагмент ритуального копья</a> (можно подобрать перед собором) или <a href="#">золотого ритуального копья</a> — во время атаки копья вырываются из земли вокруг босса, а после ряд копий вырывается вперёд по широкой линии, какое-то время находясь в земле и нанося урон, если приблизиться к ним. При атаке босс имеет гиперброню (заметно по свечению вокруг него), но ненадолго.</li>
                <li>Призванный страж картин издалека закидывает Клинками стражей церкви (самонаводящимися синими кинжалами), активно машет саблями.</li>
                <li>Стражи картин активно используют групповое лечение.</li>
            </ul>
            <p>NPC использует в основном магические атаки и катану, наносящую очень много урона:</p>
            <ul>
                <li>В дальнем бою достаёт лук, стреляя из него (иногда почти в упор).</li>
                <li>Бьёт катаной по земле, вызывая узкую магическую волну, направленную вперёд (малая дальность).</li>
                <li>Удары катаной в ближнем бою (в том числе и заряженные силовые атаки).</li>
                <li>Быстрое комбо из пяти атак катаной, делает из стойки.</li>
            </ul>
            <p>Человек не имеет возможности использовать предметы лечения, такие как <a href="#">эстус</a>, <a href="#">скрытое благословение</a>, <a href="#">небесное благословение</a>, <a href="#">сигбрау</a> и т.п., но может лечиться при помощи <a href="#">Чудес</a>. Количество дружественных фантомов определяет защиту служителя босса. При сражении четырёх человек против босса его защита будет крайне высокой, а наличие ранга 1 в ковенанте Копья церкви только усугубит ситуацию. Не рекомендуется призывать больше двух фантомов.</p>
            <h2>Тактика против Копья церкви</h2>
            <ul>
                <li>Очень подвижный босс с разнообразными атаками, наносящими много урона. В бою поможет оружие, имеющее большой радиус атаки (копья, кнуты — хорошим выбором будут <a href="#">Ведьмины волосы</a>) — это оружие будет хорошо сбивать атаки как босса, так и призванного фантома, при этом держа их на расстоянии. Неплохим вариантом для битвы станет <a href="#">алебарда Гундира</a> или <a href="#">Асторский двуручный меч</a>. Спецприём этого оружия (c R2), помимо существенного урона, гарантированно сбивает с ног как NPC-босса, так и призванных стражей картин. Босса можно легко убить за 3-4 комбо <a href="#">Большой косой Фриде</a>, так как у него лёгкое оружие, и этот спецприём он не сможет сбить. Также можно использовать <a href="#">Мечи рыцарей из города за стеной</a>, что падают с противника, который стоит недалеко от арены с боссом. Их разгонная атака с двух рук с последующим комбо наносит боссу чудовищный урон, к тому же он никак не способен ей сопротивляться.</li>
                <li>С начала боя лучше в первую очередь сосредоточить внимание на страже картин, так как он будет мешать своими сериями атак и активно лечить босса, если не убить его сразу. Крайне важно уничтожить первого стража картин до призыва босса.</li>
                <li>После потери боссом определённого количества здоровья (чуть больше трети) Арго, даже будучи мёртвым, вновь призовёт второго стража картин. Его стоит убить сразу и не давать ему использовать лечение — это восполнит боссу больше половины от того, сколько вы успели ему сбить. Второй страж картин появится независимо от наличия первого, т.е. если Вы не успеете убить Первого, а будете атаковать исключительно босса, велика вероятность, что в бою будут участвовать ОБА стража, что сведёт ваши шансы на победу к минимуму.</li>
                <li>Можно довольно просто убить, наложив <a href="#">токсин</a>. После убийства первого стража картин зайти за колонну и ждать, пока он приблизится. За колонной его атаки не достанут вас, однако токсин проходит через неё, и даже при касте под себя есть вероятность, что токсин отравит босса, так как колонна небольшая, и облако просто не успеет рассеяться прежде, чем босс дойдёт до него (вашего предыдущего положения), огибая колонну.</li>
            </ul>
            <h2>Тактика за Копьё церкви</h2>
            <ul>
                <li>В первую очередь необходимо понимать, что у вас не будет возможности восполнять здоровье предметами (эстус, скрытое благословение, небесное благословение, сигбрау и т.п.), в то время как ваши оппоненты будут активно использовать эстус, но всё же можно подлечиться, подойдя к стражу картин, когда он использует лечение.</li>
                <li>Немаловажным фактором является количество врагов и ваш ранг в ковенанте — чем больше врагов, тем больше защиты. Первый ранг также даёт прибавку к защите. В связи с вышеуказанными особенностями, возможность использования чудес типа лечения и повышения защиты становится очень весомым доводом в сражении.</li>
                <li>Поскольку временами над вами будут появляться сферы света, наводящиеся во врагов и наносящие урон молнией, стоит воспользоваться этим для восполнения здоровья, либо атаки врага. Обездвиживание + сферы света способны быстро уничтожить одного из оппонентов.</li>
                <li>Также не стоит забывать об уникальном предмете, доступном только членам ковенанта — <a href="#">Фрагменте ритуального копья</a>. Правильное время атаки и позиционирование может не только отрезать врага от его сопартийцев, но и послужить отличной защитой (как для вас, так и для стражей картин) при лечении.</li>
                <li>Рассчитывайте, что в бою против вас будет 2-3 человека, поэтому, чтобы иметь равные шансы, охраняйте второго стража картин (первый скорее всего умрёт в момент вашего призыва, если нет — вы с большой вероятностью победите).</li>
                <li>Будучи призванным в качестве босса, у вас будет некое количество баланса, которое можно использовать для боя. То есть не каждый удар может вас сразу бросить в стан.</li>
                <li>Второй страж появляется не сразу, а при нанесении вам определённого урона или по прошествии времени.</li>
                <li>Хорошо помогает <a href="#">Кольцо стальной защиты</a> — учитывая естественную прибавку к поглощению урона, является неплохим бонусом к защите, позволяющим получать ещё меньше урона, особенно в схватке с двумя и большим числом игроков.</li>
                <li>Неизвестно, баг или нет: в обличии босса можно спокойно вернуться в свой мир с помощью <a href="#">Чёрного кристалла разделения</a>, при этом в мир нарушителя сна тут же призовётся Сумрак.</li>
            </ul>
