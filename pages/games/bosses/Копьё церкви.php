                <li>Босса можно воскресить. Для этого у <a href="#">Монолита очищения</a> или у <a href="#">Статуи Велки</a> нужно выбрать опцию "Исполнить королевскую волю". Цена равна вашему уровню, помноженному на 500.</li>
                <li>Существует вероятность, что в качестве босса может оказаться читер! Иногда это приводит к появлению в инвентаре нежелательных предметов, за которые может последовать бан аккаунта.</li>
