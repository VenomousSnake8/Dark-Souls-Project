            <h2>Описание</h2>
            <p><a href="#">Копья церкви</a> — древний <a href="#">ковенант</a>, защищающий сон принцессы Филианоры. Босс — защитник, призванный судьёй Арго.</p>
            <p>Уникальность этой битвы в том, что в качестве босса с высокой степенью вероятности (в онлайне) будет выступать другой игрок. При этом он не может использовать обычный эстус, но зато имеет возможность применять <a href="#">наконечник ритуального копья</a> либо <a href="#">наконечник божественного копья</a>.</p>
